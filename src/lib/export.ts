export function exportLocalCSV(data: Record<string, unknown>, subTool: string) {
  let rows: string[][] = []
  if (subTool === 'entities') {
    const d = data as { priority_additions?: Array<{ entity: string; type: string; why: string; example_sentence: string }> }
    rows = [
      ['Entity', 'Type', 'Why Important', 'Example Sentence'],
      ...(d.priority_additions ?? []).map(e => [e.entity, e.type, e.why, e.example_sentence]),
    ]
  } else if (subTool === 'queries') {
    const d = data as { queries?: Array<{ query: string; platform: string; intent_type: string; coverage_score: number; verdict: string; gap: string; fix: string }> }
    rows = [
      ['Query', 'Platform', 'Intent', 'Coverage Score', 'Verdict', 'Gap', 'Fix'],
      ...(d.queries ?? []).map(q => [q.query, q.platform, q.intent_type, String(q.coverage_score), q.verdict, q.gap, q.fix]),
    ]
  } else if (subTool === 'gbp') {
    const d = data as { gbp_posts?: Array<{ type: string; title: string; body: string; cta: string }>; qa_pairs?: Array<{ question: string; answer: string }> }
    rows = [
      ['Type', 'Title / Question', 'Body / Answer', 'CTA'],
      ...(d.gbp_posts ?? []).map(p => ['Post - ' + p.type, p.title, p.body, p.cta]),
      ...(d.qa_pairs ?? []).map(q => ['Q&A', q.question, q.answer, '']),
    ]
  } else {
    rows = [['Key', 'Value'], ...Object.entries(data).map(([k, v]) => [k, JSON.stringify(v)])]
  }
  downloadCSV(`semantictoolz-local-${subTool}.csv`, rows)
}

