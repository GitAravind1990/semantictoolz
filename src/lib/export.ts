'use client'

// ── CSV export ────────────────────────────────────────────────────────────────
function downloadCSV(filename: string, rows: string[][]) {
  const csv = rows.map(r => r.map(c => `"${String(c ?? '').replace(/"/g, '""')}"`).join(',')).join('\n')
  const blob = new Blob([csv], { type: 'text/csv;charset=utf-8;' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url; a.download = filename; a.click()
  URL.revokeObjectURL(url)
}

// ── PDF export (via print dialog) ────────────────────────────────────────────
function downloadPDF(filename: string, htmlContent: string) {
  const win = window.open('', '_blank')
  if (!win) return
  win.document.write(`<!DOCTYPE html><html><head>
    <title>${filename}</title>
    <style>
      body { font-family: Arial, sans-serif; font-size: 12px; color: #111; padding: 24px; max-width: 800px; margin: 0 auto; }
      h1 { font-size: 20px; color: #1e3a8a; border-bottom: 2px solid #2563eb; padding-bottom: 8px; margin-bottom: 16px; }
      h2 { font-size: 15px; color: #1e3a8a; margin: 16px 0 6px; }
      table { width: 100%; border-collapse: collapse; margin: 12px 0; font-size: 11px; }
      th { background: #1e3a8a; color: white; padding: 6px 8px; text-align: left; }
      td { padding: 5px 8px; border-bottom: 1px solid #e5e7eb; }
      tr:nth-child(even) td { background: #f8fafc; }
      .badge { display: inline-block; padding: 2px 8px; border-radius: 99px; font-size: 10px; font-weight: bold; }
      .high { background: #fee2e2; color: #dc2626; }
      .medium { background: #fef3c7; color: #d97706; }
      .low { background: #f0fdf4; color: #16a34a; }
      .likely { background: #f0fdf4; color: #16a34a; }
      .possible { background: #fef3c7; color: #d97706; }
      .unlikely { background: #fee2e2; color: #dc2626; }
      p { margin: 4px 0 8px; line-height: 1.5; }
      .meta { color: #6b7280; font-size: 10px; margin-bottom: 16px; }
      @media print { body { padding: 0; } }
    </style>
  </head><body>${htmlContent}<script>window.onload=()=>{window.print();}</script></body></html>`)
  win.document.close()
}

// ── DOCX export (using docx npm package loaded via CDN) ───────────────────────
async function downloadDOCX(filename: string, buildFn: () => Promise<Uint8Array>) {
  const bytes = await buildFn()
  const blob = new Blob([bytes], { type: 'application/vnd.openxmlformats-officedocument.wordprocessingml.document' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url; a.download = filename + '.docx'; a.click()
  URL.revokeObjectURL(url)
}

