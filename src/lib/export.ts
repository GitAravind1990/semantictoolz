  rows.push([''], ['CONTENT CALENDAR'], ['Week', 'Title', 'Type', 'Why Now'])
  ;(data.content_calendar ?? []).forEach(c => rows.push([`Week ${c.week}`, c.title, c.type, c.why_now]))
